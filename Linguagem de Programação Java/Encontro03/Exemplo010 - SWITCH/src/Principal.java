
public class Principal {

	public static void main(String[] args) {

		char valor = 'a';
		
		switch (valor) {
		
		case 'A':
			System.out.println("Voce digitou A");
			break;

		case 'a':
			System.out.println("Voce digitou A");
			break;	
			
		case 'E':
			System.out.println("Voce digitou E");
			break;
		case 'e':
			System.out.println("Voce digitou E");
			break;
		}
		
		System.out.println("Fim");
		
	}

}
